function initNavigation() {
  const panel = document.getElementById('nav-links');
  const toggle = document.querySelector('.floating-nav__toggle');
  if (!panel || !toggle) return;
  const sections = [
    { id: 'foreword', label: '前言' },
    { id: 'awakening', label: '泡发大师指南' },
    { id: 'quick-reference', label: '烹饪宝典' },
    { id: 'recipe-explorer', label: '食谱灵感探索器' }
  ];
  ingredientChapters.forEach((chapter) => {
    sections.push({ id: `ingredient-${chapter.id}`, label: chapter.name });
  });

  panel.innerHTML = '';
  sections.forEach((section) => {
    const link = document.createElement('a');
    link.href = `#${section.id}`;
    link.textContent = section.label;
    panel.appendChild(link);
  });

  toggle.addEventListener('click', () => {
    const expanded = toggle.getAttribute('aria-expanded') === 'true';
    toggle.setAttribute('aria-expanded', String(!expanded));
    panel.style.display = expanded ? 'none' : 'flex';
  });

  document.addEventListener('click', (event) => {
    if (!panel.contains(event.target) && event.target !== toggle) {
      toggle.setAttribute('aria-expanded', 'false');
      panel.style.display = 'none';
    }
  });

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        const id = entry.target.getAttribute('id');
        const link = panel.querySelector(`a[href="#${id}"]`);
        if (link) {
          if (entry.isIntersecting) {
            panel.querySelectorAll('a').forEach((a) => a.classList.remove('active'));
            link.classList.add('active');
          }
        }
      });
    },
    { threshold: 0.35 }
  );

  sections.forEach((section) => {
    const target = document.getElementById(section.id);
    if (target) {
      observer.observe(target);
    }
  });
}

