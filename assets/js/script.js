function renderQuickReference(filter = 'all') {
  const tbody = document.querySelector('#reference-table tbody');
  if (!tbody) return;
  tbody.innerHTML = '';
  const rows = quickReference.filter((item) => {
    if (filter === 'all') return true;
    return item.methodTags.includes(filter);
  });

  rows.forEach((item) => {
    const tr = document.createElement('tr');
    tr.innerHTML = `
      <td>${item.ingredient}</td>
      <td>${item.flavor}</td>
      <td>${item.method}</td>
      <td>${item.time}</td>
      <td>${item.notes}</td>
    `;
    tbody.appendChild(tr);
  });
}

function initQuickReferenceFilter() {
  const select = document.getElementById('method-filter');
  if (!select) return;
  const methodSet = new Set();
  quickReference.forEach((item) => {
    item.methodTags.forEach((tag) => methodSet.add(tag));
  });
  methodSet.forEach((method) => {
    const option = document.createElement('option');
    option.value = method;
    option.textContent = method;
    select.appendChild(option);
  });
  select.addEventListener('change', (event) => {
    renderQuickReference(event.target.value);
  });
}

